<h1>Zammad and Nextcloud announce collaboration at Nextcloud Conference</h1>
<p><em>October 3, 2020, Berlin</em> - Zammad GmbH, the amazing open source Help Desk and Ticket System and Nextcloud GmbH, provider of the most popular on-premises content collaboration platform announce the result of integration efforts at the Nextcloud Conference in Berlin, Germany. Both platforms are available under open source licenses and designed for on-premises hosting or cloud hosting, giving customers control over security, compliance and cost factors and protecting their digital sovereignty.</p>
<p>Just a rough quote proposal:</p>
<blockquote>Zammad enables users to manage tasks and incoming customer requests. Open source, self-hosted cloud technologies allow networking and integration with each other. Nextcloud and Zammad complement each other and integration provides users with improved productivity.</blockquote>
<p>-- Martin Edenhofer, Founder and CEO at Zammad GmbH</p>
<p>With Nextcloud 20, a Zammad integration app provides a Dashboard widget with a Zammad ticket overview, support for finding Zammad tickets using Nextcloud’s new unified search and notifications on status updates to tickets. The integration features make it easier for Nextcloud users to track the state of Zammad tickets, saving time and improving response times to customers.</p>
<blockquote>With Nextcloud we offer a platform for real-time collaboration and communication. The new Zammad integration capabilities in Nextcloud create a more integrated experience for users, bringing the two platforms closer together. Users can see urgent tasks on their dashboard and plan work in the morning, quickly find issues using the search or get notified about customer responses.</blockquote>
<p>-- Frank Karlitschek, Founder and CEO at Nextcloud GmbH</p>
<p>The integration capabilities were demonstrated in a keynote by Nextcloud CEO Frank Karlitschek this weekend at the Nextcloud Conference in Berlin.</p>
<p>The integration app is available from the Nextcloud app store for the just released Nextcloud 20 right now. Customers integrated in a integrated Zammad and Nextcloud environment can contact Zammad GmbH or Nextcloud GmbH.</p>
<h2>About Nextcloud</h2>
<p>Nextcloud Hub is the industry-leading, fully open source, on-premises team productivity platform, combining the easy user interface of consumer-grade cloud solutions with the security and compliance measures enterprises need. Nextcloud Hub brings together universal access to data through mobile, desktop and web interfaces with next-generation, on-premise secure communication and collaboration features like real-time document editing, chat and video calls, putting them under direct control of IT and integrated with existing infrastructure.</p>
<p>Nextcloud’s easy and quick deployment, open, modular architecture and emphasis on security and advanced federation capabilities enable modern enterprises to leverage their existing file storage assets within and across the borders of their organization. For more information, visit <a href="https://nextcloud.com">nextcloud.com</a> or follow <a href="https://twitter.com/Nextclouders">https://twitter.com/Nextclouders</a> on Twitter.</p>
<h2>About Zammad</h2>
<p>Zammad is the open-source helpdesk and customer support platform for companies large and small. Zammad’s user interface was developed in collaboration with the award-winning UI experts from Zeughaus. It breaks with old traditions and is based on the familiar use of web browsers and smartphones, making it intuitive and easy-to-use. Behind the project is an experienced development team led by Martin Edenhofer, who launched the commercially successful, enterprise-scale OTRS ticket system in 2001.</p>
