    <h2>In the building</h2>
    <h3>Hackathon</h3>
    <p>Friday, registration and hacking rooms open at 9. Walk from registration desk to the elevators. This is where we are:
    <ul>
        <li>5th floor:  food-and-drink room <code>MA542</code> and <a href="https://github.com/nextcloud/core/wiki/Conference-meetings">meeting room</a> <code>MA548</code></li>
        <li>6th floor the hacking rooms: <code>MA642</code> (<em>starters room</em>), <code>MA644</code>, <code>MA648</code>, <code>MA649</code> and <code>MA650</code></li>
        <li>8th floor:interview room (<code>MA841</code>)</li>
        <li>All the way to the top and up the stairs one more floor for lunch (with your food ticket!) and a great view over the city in the restaurant</li>
    </ul>
    </p>
    <h3>Conference days - Saturday and Sunday</h3>
    <p>Registration opens at 9 and at 10 we start with opening, keynotes and lightning talks in room <code>MA004</code>, main hall of the »<em>Mathematik</em>« building, ground floor. After lunch the hacking rooms open while the main hall features workshops in the <code>MA004</code>, <code>MA005</code> and <code>MA144</code> rooms.</p>
    <p>Find the <a href="https://conference.nextcloud.org/conference/oCC2015/schedule">conference program here</a>.</p>
    </div>
</div>

<div class="row conf-info">
    <div class="col-md-6">
      <h2>Connectivity and power</h2>
    <p>We've got wifi and many rooms are equipped with a (limited) number of network cables.</p>
    <p>At registration, you sign up for a guest account. Use the how-to's here to connect:
    <ul>
        <li><a href="https://www.tubit.tu-berlin.de/wlan/">in German</a></li>
        <li><a href="https://www.tubit.tu-berlin.de/wlan/parameter/en/">in English</a></li>
    </ul>
    <p>We have power strips in most places but they're EU plugs - don't forget your adapters!</p>
    </div>
    <div class="col-md-6">
    <h2>Getting stuff done and finding who to talk to</h2>
    <p>New to Nextcloud, looking for an introduction or answers about APIs, infrastructure or a pull request?
    <br/> <strong>ask your questions in the <em>starters room</em> <code>MA642</code></strong></p>
    <p>If you want to make sure a certain topic is discussed, make an agreement with the relevant people and <a href="https://github.com/nextcloud/core/wiki/Conference-meetings">book the meeting room</a>.</p>
    <p>If you are looking for some non-hacking entertainment in Berlin, <a href="https://github.com/nextcloud/core/wiki/Conference-non-hacking-Entertainment">check our wiki page</a>!</p>
    </div>
</div>

