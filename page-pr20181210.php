<h1>Nextcloud 15 introduces social networking, next-gen 2-factor authentication and innovative collaborative document editing abilities </h1>
<h3>Release also adds new design and grid view, workflow features and 2-3x faster loading performance in big step for self-hosted file exchange and collaboration platform</h3>

<p><em>December 10, 2018 - Stuttgart.</em> Nextcloud GmbH, the globally leading supplier of the most popular self-hosted content collaboration platform, announces immediate availability of Nextcloud 15. This release introduces the Social app, a distributed, federated social network app using the Activity Pub federation standard. Security of Nextcloud servers is improved with updates to our two-factor authentication, making its use easier through 2FA-via-notifications and more secure by allowing system administrators to enforce its use.</p>

<p>The Nextcloud Social app allows users to share thoughts on an activity feed and subscribe to the feeds of others, giving organizations the ability to build an enterprise social network to improve internal social cohesion and communication. Nextcloud servers can join the fediverse, an interconnected and decentralized network of independently operated servers communicating through the Activity Pub standard. This includes Mastodon servers, the popular open source twitter competitor, letting users on Nextcloud and Mastodon share and discuss ideas.</p>

Eugen Rochko, Mastodon founder:
<blockquote>The Mastodon project is excited that Nextcloud users can now join the fediverse, potentially growing the decentralized web by an order of magnitude</blockquote>

<p>Increasing the security of Nextcloud servers is the ability for server administrators to enforce two-factor authentication, while making it easier for users by allowing them to approve a login with a simple button click in the notification of an existing device. Improved hardening protects users from cross-site scripting vulnerabilities with more strict CSP and updated App token handling simplifies re-authorization of clients.</p>

Roeland Douma, head of the Nextcloud Security team:
<blockquote>Security continues to be a key concern to Nextcloud customers. While our leading security bug bounty program provides a great degree of confidence in our ability to continuously write secure, quality code, introducing new security technologies and tightening existing restrictions are a key part of our strategy.</blockquote>

<p>Extensive work with Collabora Productivity improved the design and integration of Collabora Online in Nextcloud, bringing a new sidebar which allows audio/video calls, sharing and commenting during edit sessions and integrates Nextcloud versions as document revisions. Design improvements to the toolbars, HiDPI support and thumbnail rendering as well as much improved mobile experience make document editing in the office or on the go easier than ever. These improvements will become available as releases of the respective apps are made over the coming weeks.</p>

<p>Sharing improvements allow creation of any number of public share links, each protected with individual passwords, access rights, optional Video Verification and the new "hide download" option which lets recipients view but not download documents.</p>

Andreas Rode, Head of Sales at Nextcloud GmbH:
<blockquote>In a little over two years, Nextcloud has evolved to become the most sought-after self-hosted content collaboration and productivity platform with hundreds of thousands of servers and tens of millions of users. With Nextcloud 15, the stage is set for continued growth by delivering on the needs of private and enterprise users alike.</blockquote>

<p>Nextcloud 15 contains many more improvements, including:</p>

<ul>
    <li>design improvements, introducing a new grid view with large file previews, improved text readability with a new font, updated button and tab design and accessibility improvements</li>
    <li>two new workflow apps, enabling easy PDF conversion and running scripted operations on files when specified conditions are met</li>
    <li>a much improved Dashboard app giving users a quick overview of the day</li>
    <li>updates to Nextcloud Talk including new notification settings, ability to invite email addresses to conversations, integration in the Nextcloud sidebar and more</li>
    <li>rewritten dashboard app with new widgets API for developers</li>
    <li>updated Android app with direct editing files with Collabora, sharing files to a Talk room, hiding download option when sharing, adding notes to shares, search in files and more</li>
    <li>updated iOS app with direct editing files with Collabora, video/photo taking in the app, offline file handling and more</li>
    <li>a 2-3x faster loading of the web interface, among other performance improvements</li>
</ul>

<p><strong>About Nextcloud</strong><br>
Nextcloud offers the industry-leading, fully open source, self-hosted Content Collaboration Platform, combining the easy user interface of consumer-grade cloud solutions with the security and compliance measures enterprises need. Nextcloud brings together universal access to data through mobile, desktop and web interfaces with next-generation, on-premise secure communication and collaboration features like real-time document editing, chat and video calls, putting them under direct control of IT and integrated with existing infrastructure.<br/>
Nextcloud's easy and quick deployment, open, modular architecture and emphasis on security and advanced federation capabilities enable modern enterprises to leverage their existing file storage assets within and across the borders of their organization. For more information, visit <a href="https://nextcloud.com">nextcloud.com</a> or follow <a href="https://twitter.com/Nextclouders">@Nextclouders</a> on Twitter.</p>


