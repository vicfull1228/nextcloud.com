<h1>Nextcloud near to double bookings in 2020, invests in expanding for 2021</h1>
<p><em>Stuttgart, February 22, 2020</em> - Nextcloud GmbH, the vendor behind the most deployed on-premises content collaboration platform, nearly doubled bookings in 2020 over 2019, increased its customer base by over 50%, and is expanding its team quickly to handle the growing demand. Continued innovation with a strong focus on meeting the real-life needs of modern organizations during the move to remote collaboration over 2020 has been key to this growth.</p>
<blockquote>
<p>2020 has been a difficult year for many organizations. We have been fortunate to be able to help many in the transition to remote work and all our efforts were poured into further improving Nextcloud to support the day to day work of our customers. 2020 has further increased our lead in the market and we look forward to significant expansion in 2021.</p>
</blockquote>
<p>-- Frank Karlitschek, CEO and founder of Nextcloud</p>

<h2>Continued growth supported by industry fundamentals and strong delivery</h2>
<p>The release of Nextcloud Hub early in 2020 tripled the download rate of Nextcloud and doubled lead flow, showing strong interest in the market. During March and April, those numbers rose by another 4x as many organizations kicked off projects with Nextcloud to enable remote work during the first lock-down. Much of the rest of the year was focused on delivering on these projects and supporting customers with the increased need for real-time, remote collaboration. Nextcloud Talk in particular saw incredible growth, increasing bookings over 100 fold over 2019, which was already a strong year for Talk. Nextcloud Hub 19 delivered a wide range of technical improvements in Talk, further supporting this growth.</p>
<p>With a 50% growth in customer base and a near doubling of total bookings, Nextcloud managed a healthy growth in average subscription value. A continued strong focus on subscriptions over consulting and an increase of the minimum base subscription to 100 users is expected to further improve these numbers over 2021. The sales team has expanded by 50% to service new customers and this growth rate is planned to continue.</p>
<p>After crossing the 100M pulls threshold in 2019, the Nextcloud Docker image passed the 500M benchmark in 2020. With an estimated 50% growth in the installed base, a 40% organic growth in website visits, 34% growth in community code contributions, and a 40% increase in Nextcloud apps to nearly 300, the wider Nextcloud ecosystem continued its healthy development. Google trends shows search interest in Nextcloud averaging almost 5x over its closest competition. With over 3000 code contributors over its lifetime and more than 700 in the last year as well as over 300 apps, Nextcloud is the largest self-hosted productivity platform ecosystem.</p>
