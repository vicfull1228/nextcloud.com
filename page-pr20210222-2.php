<h2>Partnerships key to success</h2>
<p>The Nextcloud Conference was the incredibly successful virtual venue for Nextcloud Hub 20, a release build in collaboration with over half a dozen partners. The collaboration with industry vendors will continue, both on a technical level with deeper third-party integration in Nextcloud, and in business, as exemplified in our partnership announcement with European IT services giant Deutsche Telekom in early 2021.</p>
<p>The release of Nextcloud Hub 21 today, with the innovative High Performance Back-end for Files and significant collaboration improvements will further extend the technological and market lead Nextcloud has over its competition.</p>

