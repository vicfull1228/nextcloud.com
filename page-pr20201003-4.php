<h1>Moodle and Nextcloud announce further integration steps at Nextcloud Conference</h1>
<p><em>October 3, 2020, Berlin</em> - Moodle Pty Ltd., the premier open source learning management platform and Nextcloud GmbH, provider of worlds’ most popular on-premises content collaboration platform announce the result of further integration efforts at the Nextcloud Conference in Berlin, Germany. Both platforms are available under open source licenses and designed for on-premises hosting or cloud hosting, giving customers control over security, compliance and cost factors and protecting their digital sovereignty. A keynote by Moodle founder and CEO Martin Dougiamas will go deeper into details of the collaboration on the second day of the conference.</p>
<blockquote>The crisis has increased the need for online collaboration solutions and Nextcloud and Moodle are extremely popular in the educational space. An expansion of our integration as initially developed in 2017 is set to benefit millions of students and teachers.</blockquote>
<p>-- Martin Dougiamas, Founder/CEO at Moodle Pty Ltd.</p>
<p>In 2017, the Nextcloud Education Edition debuted support for file handling lintegration in Moodle. In collaboration with Moodle, Nextcloud has extended the integration with Moodle, implementing a Moodle “mobile web service” authentication flow, integrated a Nextcloud Dashboard widget showing upcoming events and recently accessed courses from Moodle and developed a search integration that can search for courses and other content in Moodle.</p>
<blockquote>Together, Moodle and Nextcloud offer educators a complete platform for remote learning and connecting to students. The new integration capabilities create a more integrated experience for users, bringing the two platforms closer together.</blockquote>
<p>-- Frank Karlitschek, Founder and CEO at Nextcloud GmbH</p>
<p>The new integration capabilities are available from the Nextcloud app store for the just released Nextcloud 20 right now. Customers integrated in a integrated learning management and content collaboration platform can contact Moodle Pty Ltd. or Nextcloud GmbH.</p>
<p>A roadmap for further integration between Moodle and Nextcloud has been defined and upcoming Moodle and Nextcloud releases will deliver a further improved user experience.</p>
<h2>About Nextcloud</h2>
<p>Nextcloud Hub is the industry-leading, fully open source, on-premises team productivity platform, combining the easy user interface of consumer-grade cloud solutions with the security and compliance measures enterprises need. Nextcloud Hub brings together universal access to data through mobile, desktop and web interfaces with next-generation, on-premise secure communication and collaboration features like real-time document editing, chat and video calls, putting them under direct control of IT and integrated with existing infrastructure.</p>
<p>Nextcloud’s easy and quick deployment, open, modular architecture and emphasis on security and advanced federation capabilities enable modern enterprises to leverage their existing file storage assets within and across the borders of their organization. For more information, visit <a href="https://nextcloud.com">nextcloud.com</a> or follow <a href="https://twitter.com/Nextclouders">https://twitter.com/Nextclouders</a> on Twitter.</p>
<h2>About Moodle</h2>
<p>First released in 2001 by Martin Dougiamas, Moodle has provided educators, workplaces and businesses with powerful tools to manage and promote learning online. Today, Moodle is the most widely used learning management system in the world, with over 80 million registered users in more than 230 countries. Supported by a global network of 85 certified Moodle Partnerships, the Moodle learning management system continues to offer collaborative and innovative online learning technology to a global audience. For more information, visit <a href="https://moodle.com">moodle.com</a></p>
