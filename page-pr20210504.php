<h1>OpenProject and Nextcloud announce collaboration and integration features</h1>
<p><em>Integration connects project management solution to content collaboration platform, exchanging information and facilitating smooth project management</em></p>
<p><em>Stuttgart, May 4, 2021</em> - Nextcloud GmbH, the company behind the worlds’ most deployed on-premises content collaboration platform, and OpenProject GmbH, the leading free and open source project management software, announce the results of initial integration efforts, with further work in development. The integration will improve the productivity of enterprise users without requiring them to risk compliance and data leaks by losing control over their content to foreign SaaS providers.</p>
<blockquote>
<p>We develop open source project management software to change the way how people collaborate location-independently on projects that have a positive impact on our daily lives. What makes OpenProject stand out is our commitment to data protection and information security. The partnership with Nextcloud is another step on our path to providing data sovereignty to our customers by combining the best of powerful open source applications. It fills me with great joy to join forces with such a strong partner in the open source community that shares the same motivation.</p>
</blockquote>
<p>-- Niels Lindenthal, Founder and CEO at OpenProject GmbH</p>
<p>In an environment of growing concerns about digital sovereignty, enterprises increasingly rely on on-premises productivity solutions as offered by the market-leading content collaboration platform provider Nextcloud and the leading free and open source project management software, OpenProject. Integration of OpenProject data in the Nextcloud Dashboard offers users a view of ongoing projects, as well as showing them notifications and enabling the built-in universal search to find project information from OpenProject.</p>
<blockquote>
<p>Enterprises require efficient, well integrated platforms that put their users’ productivity first, empowering them to access and work with their data and communicate wherever they are. At the same time, control over data flows and compliance are key concerns to our common customers. This exciting partnership will enable more efficient collaboration and we look forward to further results of our integration efforts and how our customers will be able to benefit from these.</p>
</blockquote>
<p>-- Frank Karlitschek, Founder and CEO at Nextcloud GmbH</p>
<p>In collaboration with mutual customers, further integration efforts are under way, which will deliver Nextcloud integration on the OpenProject side.</p>
<p>The OpenProject/Nextcloud integration app can be found in the Nextcloud app store. The results of further integration efforts are expected later in 2021.</p>
<h2>About Nextcloud</h2>
<p>Nextcloud Hub is the industry-leading, fully open source, on-premises team productivity platform, combining the easy user interface of consumer-grade cloud solutions with the security and compliance measures enterprises need. Nextcloud Hub brings together universal access to data through mobile, desktop and web interfaces with next-generation, on-premise secure communication and collaboration features like real-time document editing, chat and video calls, putting them under direct control of IT and integrated with existing infrastructure.</p>
<p>Nextcloud’s easy and quick deployment, open, modular architecture and emphasis on security and advanced federation capabilities enable modern enterprises to leverage their existing file storage assets within and across the borders of their organization. For more information, visit <a href="https://nextcloud.com">nextcloud.com</a> or follow <a href="https://twitter.com/Nextclouders">https://twitter.com/Nextclouders</a> on Twitter.</p>
<h2>About OpenProject</h2>
<p>OpenProject is the leading free and open source project management software. Its vision is to empower project teams to achieve great things together for the good of society.</p>
<p>OpenProject supports classic, agile as well as hybrid project management along the entire project life-cycle. Its team is specialized in the continuous development and improvement of the open source software, delivering an up-to-date, secure project management software, as well as maintaining an active and involved community. OpenProject is available as both cloud and on-premises version.</p>
<p>Organizations across all industries and sizes use OpenProject due to its high commitment to data protection and information security. These are of central importance to OpenProject and are one of the main motives for the development of this open source software. OpenProject aims to become a lighthouse project for “Data privacy made in Europe”. Find out more about OpenProject on <a href="https://www.openproject.org/">openproject.org</a> or follow <a href="https://twitter.com/openproject">@openproject</a> on Twitter.</p>


