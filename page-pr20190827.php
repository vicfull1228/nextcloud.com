<h1>Amid escalating trade disputes, EU governments choose independence from US cloud providers</h1>

<h3>Increasing deployments of private clouds sign EU is taking digital sovereignty more serious</h3>

<p><em>Stuttgart, August 27, 2019</em> - In response to trade tensions escalating including the recent dispute over a digital tax in Europe, the introduction of the GDPR and legal challenges to the US Cloud Act, the French ministry of Interior, the Dutch ministry of Education and Swedish federal government agencies have joined the German federal government in moving away from US cloud solutions and instead deploying a European-designed, private cloud solution.</p>

<blockquote>The French government cares deeply about the safety of the data of their citizens and employees. With the on-premises content collaboration platform Nextcloud we have opted for a secure, easy to use solution from the leading European vendor.</blockquote>

<p>Said Thierry Markwitz, Sous Directeur des Infrastructures au Ministère de l’Interieur</p>

<p>During the last year, the French Ministry of Interior has evaluated building an internal cloud and is now preparing a roll-out of a production-ready Nextcloud to their users, designed to scale to the 300.000 employees of the ministry. These users will have access to easy, secure file exchange with mobile, desktop and web access and real-time collaborative office document editing.</p>

<p>According to the German Federal Government, ITZBund:</p>

<blockquote>Nextcloud provides users with a modern, easy-to-use and productivity-oriented solution that enables efficient online and mobile collaboration and communication. ITZBund now provides an on-premises open source solution from a German provider to keep control over their own data.</blockquote>

<p>After ITZBund started a pilot for about 5000 users, testing the functionality and reliability of the service, it began deployment of Nextcloud for the about 300.000 employees of the entire German Federal Government. Strict security requirements were crucial for choosing Nextcloud as the file sync and share solution. Another important concern was scalability both in terms of large numbers of users and extensibility with additional features, for which Nextcloud offers its powerful Apps concept with over 200 apps available in our app store.</p>

<blockquote>The need for Sweden as a country to have control over its information and the consequences of not having access to the information should be considered from the perspective of national security and sovereignty. In an international crisis or conflict, the willingness and ability of other countries to access, manipulate or deny access to information handled by a service provider should be considered.</blockquote>

<p>As noted by the Swedish National Procurement Services</p>

<p>Försäkringskassan is working on developing user functionality for such things as file storing internal messaging services, end-to-end encrypted and self hosted.</p>

<blockquote>The uncertainty about how the public sector may use cloud services creates problem</blockquote>

<p>Said Försäkringskassans cto, Mikael Norberg</p>

<blockquote>It is great to see European government organizations, from local municipalities and state agencies to the federal level, take the security and privacy of their citizens' data serious and deploy our self-hosted productivity solutions.</blockquote>

<p>Said Frank Karlitschek, CEO and founder, Nextcloud GmbH</p>

