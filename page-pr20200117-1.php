<h1 id="ionosundnextcloudgemeinsamfrmehrdatensouvernitt"><strong>IONOS und Nextcloud gemeinsam  für mehr Datensouveränität</strong></h1>

<h2 id="madeingermanyundsicherkollaborationslsungvonnextcloudwirdzuknftiginderionosenterprisecloudgehostet"><strong>Made in Germany und sicher: Kollaborationslösung von Nextcloud wird zukünftig in der IONOS Enterprise Cloud gehostet</strong></h2>

<p><strong>Berlin, 17. Januar 2020.</strong> IONOS, der größte europäische Cloud- und Hostingprovider und Nextcloud, Anbieter der weltweit am weitest verbreiteten Kollaborationsplattform auf Open Source Basis, gehen eine exklusive strategische Partnerschaft ein. Ziel der Kooperation ist es, die Angebote der beiden Unternehmen zu verknüpfen, um den Anwendern die maximal mögliche Souveränität über ihre Daten zu bieten.</p>

<p>“Datensouveränität ist für viele Kunden eine unabdingbare Voraussetzung bei der Nutzung von Cloud-Diensten, um Geschäftsgeheimnisse zu schützen. Das gilt sowohl für Kunden aus dem öffentlichen Sektor als auch  der freien Wirtschaft ”, erklärt Frank Karlitschek, CEO von Nextcloud. Achim Weiß, Vorstandsvorsitzender von IONOS ergänzt: „Als deutsche Anbieter garantieren IONOS und Nextcloud ihren Nutzern die Hoheit über ihre Daten - Zugriffe durch Dritte, wie dies der US CLOUD Act ermöglicht, schließen wir aus. Unsere Kooperation gibt Nextcloud-Kunden also die Rechtssicherheit, die sie benötigen.”</p>

<p>Nextcloud-Kunden können ihre Daten und Anwendungen auf der IONOS-Infrastruktur nutzen und sie so unter voller Kontrolle selbst anpassen und zusätzliche Funktionalitäten hinzufügen. </p>

<p><strong>Deutscher Standort und Open Source als Basis für Transparenz und Datenschutz</strong></p>

<p>Sowohl IONOS wie auch Nextcloud haben ihren Hauptsitz in Deutschland und unterliegen so alleine der deutschen Gesetzgebung. Die Nutzer erhalten daher maximal mehr Schutz vor Zugriffen auf Basis des US CLOUD Acts.</p>

<p>Zudem vertrauen die beiden Unternehmen auf Open Source und unterscheiden sich damit von den großen US-Anbietern. “Die konsequente Verwendung offener Standards sorgt für Transparenz. Jeder kann zu jeder Zeit den Code einsehen, auf Sicherheitslücken prüfen und bei Bedarf auch ändern. Zudem ist nur auf Open Source-Basis die Verknüpfung der Daten und Applikationen mit anderen Systeme einfach möglich", betont Achim Weiß.</p>

<p><strong>Sensibilisierung für mehr Datensouveränität</strong> </p>

<p>In den kommenden Monaten wollen die beiden Unternehmen ihre Partnerschaft weiter vertiefen und für mehr Datensouveränität werben. "Viele Unternehmen und Behörden geben viele ihrer Daten in die Hände von Cloud-Providern aus den USA. Nextcloud und IONOS wollen gemeinsam dafür sensibilisieren, dass insbesondere empfindliche Daten besser bei europäischen Anbieter aufgehoben sind", Achim Weiß.</p>

<p>Weitere Informationen unter: <a href="https://www.ionos.de/enterprise-cloud/nextcloud">https://www.ionos.de/enterprise-cloud/nextcloud</a></p>

