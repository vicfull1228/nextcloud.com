<h1 id="ionosandnextcloudtogetherformoredatasovereignty"><strong>IONOS and Nextcloud together for more data sovereignty</strong></h1>

<h2 id="madeingermanyandsecurenextcloudcollaborationsolutiontobehostedintheionosenterprisecloud"><strong>Made in Germany and secure: Nextcloud collaboration solution to be hosted in the IONOS Enterprise Cloud</strong></h2>

<p><strong>Berlin, 17. Januar 2020.</strong> IONOS, Europe's largest cloud and hosting provider and Nextcloud, provider of the world's most deployed open source collaboration platform, are entering into an exclusive strategic partnership. The aim of the cooperation is to combine the offerings of the two companies in order to offer users the maximum possible sovereignty over their data.</p>

<p>"Data sovereignty is an essential requirement for many customers when using cloud services to protect trade secrets. This applies to customers from the public sector as well as the private sector," explains Frank Karlitschek, CEO of Nextcloud. Achim Weiß, CEO of IONOS, adds: "As German providers, IONOS and Nextcloud guarantee their users the sovereignty over their data - we rule out access by third parties as permitted by the US CLOUD Act. Our cooperation therefore gives Nextcloud customers the legal security they need".</p>

<p>Nextcloud customers can use their data and applications on the IONOS infrastructure, allowing them to customize and add additional functionality under full control.</p>

<p><strong>German location and Open Source as basis for transparency and data protection</strong></p>

<p>Both IONOS and Nextcloud are headquartered in Germany and are therefore subject to German legislation alone. Users therefore receive maximum protection against access based on the US CLOUD Act.</p>

<p>In addition, the two companies rely on open source and thus differ from the major US providers. "The consistent use of open standards ensures transparency. Anyone can view the code at any time, check it for security gaps and change it if necessary. Moreover, only on an open source basis is it easy to link data and applications with other systems," emphasizes Achim Weiß.</p>

<p><strong>Greater awareness for more data sovereignty</strong> </p>

<p>In the coming months, the two companies want to further deepen their partnership and promote greater data sovereignty awareness. "Many companies and government agencies are putting much of their data in the hands of cloud providers from the USA. Nextcloud and IONOS want to work together to raise awareness of the fact that sensitive data in particular is better off with European providers," says Achim Weiß.</p>

<p>Further information is available at: <https://www.ionos.de/enterprise-cloud/nextcloud</p>

<p><strong>About IONOS</strong></p>

<p>With more than eight million customer contracts, IONOS is the leading European provider of cloud infrastructure, cloud services and hosting services. The product portfolio offers everything that companies need to be successful in the cloud: from domains, classic websites and do-it-yourself solutions, online marketing tools to full-fledged servers and an IaaS solution. The portfolio is aimed at freelancers, business people and consumers as well as corporate customers with complex IT requirements.</p>

<p>IONOS was created in 2018 from the merger of 1&amp;1 Internet and the Berlin IaaS provider ProfitBricks. 1&amp;1 IONOS is part of the listed United Internet AG (ISIN DE0005089031). The 1&amp;1 IONOS brand family includes STRATO, Arsys, Fasthosts, home.pl, InterNetX, SEDO, United Domains and World4You.</p>

<p><strong>About Nextcloud</strong></p>

<p>Nextcloud Hub is the industry-leading, fully open source, on-premises team productivity platform, combining the easy user interface of consumer-grade cloud solutions with the security and compliance measures enterprises need. Nextcloud Hub brings together universal access to data through mobile, desktop and web interfaces with next-generation, on-premise secure communication and collaboration features like real-time document editing, chat and video calls, putting them under direct control of IT and integrated with existing infrastructure.</p>

<p>Nextcloud's easy and quick deployment, open, modular architecture and emphasis on security and advanced federation capabilities enable modern enterprises to leverage their existing file storage assets within and across the borders of their organization. For more information, visit <a href="https://nextcloud.com">nextcloud.com</a> or follow <a href="https://twitter.com/Nextclouders">@Nextclouders</a> on Twitter.</p>

