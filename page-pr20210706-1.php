<h1>Nextcloud and eID Easy partner up to deliver secure document signing to Nextcloud users</h1>
<h3><strong>eID Easy technology helps organizations using Nextcloud build efficient workflows with seamless document signing</strong></h3>
<p><em>Stuttgart, July 6th, 2021</em> - Nextcloud GmbH, the company behind the worlds’ most deployed on-premises content collaboration platform, and eID Easy, a leading gateway of trusted digital signature methods, announce integration of eID Easy signing technology in Nextcloud Hub 22, lowering the barrier to secure document signatures for Nextcloud users.</p>
<blockquote>
<p>A modern, paperless office workflow requires easy and efficient inter-organizational tracking and signing of contracts and other documents. eID Easy offers a secure way to sign PDF files and other documents inside your Nextcloud environment, and with this collaboration we bring this functionality directly to all Nextcloud users.</p>
</blockquote>
<p>-- Frank Karlitschek, CEO at Nextcloud GmbH</p>
<p>The adoption of national ID cards with electronic functions has been growing. Billions of people now have a government-issued electronic ID cards and citizens can use them to verify their identity and sign documents. eID Easy supports the different signature methods used in different countries, providing a one-stop solution for Qualified Electronic Signatures</p>
<p>One unique capability of eID Easy is to sign documents without transmitting the document to eID Easy, by signing a hash of the document.</p>
<blockquote>
<p>All-digital collaboration comes with significant associated risks in the areas of compliance, security and digital sovereignty. Nextcloud’s industry leading collaboration platform uniquely mitigates these risks and with integrated document signing, users can replicate office work flows even more efficiently in the digital, post-corona world.</p>
</blockquote>
<p>-- Margus Pala, CEO at eID Easy</p>
<p>Integrated eID Easy document signing is available as an app for Nextcloud Hub 22 in the Nextcloud app store right away, and the app can also be installed for Nextcloud 21. Nextcloud Hub 22 was released today.</p>
<h2>About Nextcloud GmbH</h2>
<p>Nextcloud Hub is the industry-leading, fully open source, on-premises team productivity platform, combining the easy user interface of consumer-grade cloud solutions with the security and compliance measures enterprises need. Nextcloud Hub brings together universal access to data through mobile, desktop and web interfaces with next-generation, on-premise secure communication and collaboration features like real-time document editing, chat and video calls, putting them under direct control of IT and integrated with existing infrastructure.</p>
<p>Nextcloud’s easy and quick deployment, open, modular architecture and emphasis on security and advanced federation capabilities enable modern enterprises to leverage their existing file storage assets within and across the borders of their organization. For more information, visit <a href="https://nextcloud.com">nextcloud.com</a> or follow <span class="citation" data-cites="nextclouders">[@nextclouders]</span>(https://twitter.com/nextclouders) on Twitter.</p>
<h2>About eID Easy OÜ</h2>
<p>eID Easy is a leading gateway of trusted digital signatures. Providing a comprehensive list of legally binding Qualified Electronic Signature methods, such as national eID cards, signature smart cards, USB crypto tokens and others, as well as the traditional simple and advanced signatures.</p>
<p>We drive businesses forward by making it easier to migrate document signing flows into a digital environment. One simple API integration, many different legally binding signature methods.</p>
<p>eID Easy’s simple and straightforward API integration allows companies to offer a variety of digital signature methods. With a focus on privacy and providing legally binding Qualified Electronic Signatures we aim to add trust to the digital capabilities of modern enterprises. For more information, visit <a href="http://eideasy.com">eideasy.com</a> or follow <span class="citation" data-cites="e_id_easy">@e_id_easy</span> on Twitter.</p>
