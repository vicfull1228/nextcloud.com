<h1>Canonical, Collabora, Nextcloud deliver work-from-home solution to Raspberry Pi and enterprise ARM users</h1>
<p><em>The internet, March 25, 2020</em> Canonical, Collabora and Nextcloud announce the immediate availability of a content collaboration platform for 64bit ARM for both consumers and enterprises. Building on the prior Nextcloud Ubuntu Appliance it adds with Collabora Online, the first viable self-hosted web office solution on the popular Raspberry Pi 4 platform.</p>
<p>The Raspberry Pi series has transformed tech, bringing down the cost of anything from IoT devices to small home servers. Ubuntu has been leading the space offering easy to install and zero-management snap software packages, lowering barrier to entry further. Interest in Nextcloud on the Raspberry Pi has been evident from hundreds of online tutorials appearing over the years, as well as enthusiasm around an earlier collaboration between Canonical, Nextcloud and Western Digital on a solution for the platform. With the introduction of the Ubuntu Nextcloud Appliance, easy deployment of the Nextcloud Hub became available for x86 devices like Intel NUC's as well as ARM devices like the Raspberry Pi, but the latter lacked support for a viable online office document editor. Today, the lack of a viable office solution is resolved with the availability of the widely used, open source, web office document editor Collabora Online.</p>
<p>This enables tens of thousands of Raspberry Pi users to turn their Pi 4 into a self-hosted content collaboration and document editing solution in minutes. With the growing availability of 64bit ARM devices in the enterprise server space, larger organizations are also set to benefit from the availability of this platform.</p>
<h2>Installation for Installation and a Tutorial</h2>
<p>Before you start, note the following system requirements for the installation:</p>
<ul>
    <li>Two USB 2.0 or 3.0 flash drives (2GB minimum)</li>
    <li>A Raspberry Pi 4</li>
    <li>A Mini HDMI to HDMI cable</li>
    <li>A monitor with VGA or HDMI interface</li>
    <li>A VGA or HDMI cable</li>
    <li>A USB keyboard and mouse</li>
    <li>A network connection with Internet access</li>
    <li>An Ubuntu 20.04 LTS desktop image</li>
</ul>
<img class="img-responsive" src="https://nextcloud.com/media/RaspberryPi-feature-img.png" alt="Logos of Nextcloud, Collabora and Canonical" />
<p>You can find a full tutorial on the Ubuntu website where we walk you through the process of installing the Nextcloud Ubuntu Appliance image on a Raspberry Pi, how to quickly set up step by step and stay fully productive by having video calls in Talk, sharing documents with Files, editing it with others using document editing office, reading your emails or planning meetings and many more!</p>
<h2>About Canonical</h2>
Canonical is the publisher of Ubuntu, the OS for most public cloud workloads as well as the emerging categories of smart gateways, self-driving cars and advanced robots. Canonical provides enterprise security, support and services to commercial users of Ubuntu. Established in 2004, Canonical is a privately held company.</p>
<h2>About Collabora</h2>
<p>Collabora Productivity created Collabora Online and is therefore the driving force behind putting LibreOffice in the cloud. Collabora provides a range of products as well as consulting to enterprise and government. Powered by the largest team of certified LibreOffice engineers in the world, Collabora is a leading contributor to the LibreOffice codebase and community. Collabora Office for Desktop and Collabora Online provide a business-hardened office suite with long-term support. Collabora’s multi-platform policy is completed with Collabora Office for iOS and Android. Collabora Productivity is a division of Collabora, the global software consultancy dedicated to providing benefits of Open Source to the commercial world, specializing in mobile, automotive and consumer electronics industries. For more information, visit <a href="https://collaboraoffice.com">www.collaboraoffice.com</a> or follow <a href="https://twitter.com/CollaboraOffice">@CollaboraOffice.</a> on Twitter</p>
<h2>About Nextcloud</h2>
<p>Nextcloud Hub is the industry-leading, fully open source, on-premises team productivity platform, combining the easy user interface of consumer-grade cloud solutions with the security and compliance measures enterprises need. Nextcloud Hub brings together universal access to data through mobile, desktop and web interfaces with next-generation, on-premise secure communication and collaboration features like real-time document editing, chat and video calls, putting them under direct control of IT and integrated with existing infrastructure.</p>
<p>Nextcloud’s easy and quick deployment, open, modular architecture and emphasis on security and advanced federation capabilities enable modern enterprises to leverage their existing file storage assets within and across the borders of their organization. For more information, visit <a href="https://nextcloud.com">nextcloud.com</a> or follow <a href="https://twitter.com/Nextclouders">https://twitter.com/Nextclouders</a> on Twitter.</p>

