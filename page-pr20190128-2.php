<h1>Nextcloud introduces Virtual Drive in Desktop Client to simplify desktop integration</h1>
<h3>Tech Preview shows virtual drive in action, reducing need for users to manage storage manually</h3>
<p><em>Stuttgart, January 28 2019</em> - Nextcloud GmbH, supplier of the most popular self-hosted content collaboration platform, introduces a Virtual Drive in the Nextcloud Desktop Client. The virtual drive, a standard technology in desktop synchronization clients, replaces the traditional files in a locally synchronized folder with a virtual view on all files the user has, available on demand and with a smart caching strategy.</p>

<blockquote>The Desktop Client is a center piece of our synchronization strategy,</blockquote> noted Frank Karlitschek, CEO of Nextcloud GmbH. <blockquote>Thus it is important we keep pushing the boundaries and deliver on what customers ask for.</blockquote>

<p>Rather than eating up all the space on the drive or forcing the user to manually handle syncing on a per-folder base, a virtual drive makes sure files are available when needed. The drive shows files as if they were available on the local system but when clicked, they are downloaded and kept available for a set time in a local cache. For users, this process is far simpler, allowing them to open files no matter what way they access them. "<em>File open</em>" dialogs, "<em>Recent files</em>" dialogs and of course the file manager like Finder or Explorer all properly handle the files.</p>

<p>The project has been in development for some time and we consider it ready for the wider community to get involved and provide feedback, testing and code contributions. The tech preview, while functional, is not recommended for daily usage. The tech preview released today works on Microsoft Windows and requires the installation of Dokan which is bundled with the installer. The Mac build will be available over the coming weeks and Linux follows after that.</p>

<p>Today Nextcloud GmbH also announced it more than doubled its customer base amid massive growth of website visits and interest in compliance solutions to secure and control data during the course of 2018. A new Customer Advisory Board was launched, with its kick-start taking place the evening before the upcoming Enterprise Day in Stuttgart, March 2019.</p>

