<h1>Nextcloud introduces Forms to protect privacy-sensitive survey data</h1>
<p><em>July 28, 2020, Stuttgart</em> - Stuttgart. Nextcloud GmbH, the company behind the worlds&#39; most deployed on-premises content collaboration platform, announces the immediate availability of Nextcloud Forms. Similar to Google Forms, this app makes it easy to create surveys for anything from market research or customer feedback to planning an office party. Privacy is an issue with surveys, as a third of people who abandon a form do so for privacy and security reasons. With Nextcloud Forms, data remains secure on the Nextcloud server and the privacy of respondents is respected.</p>
<blockquote>Surveys are very useful in a wide range of scenarios, often containing private data. A tool for creating a variety of forms that protects the privacy of those surveyed and surveying, while being easy to install and use, is exactly what the market needs.</blockquote>
<p>-- Frank Karlitschek, CEO of Nextcloud GmbH</p>
<p>The Forms app offers the ability to choose from a range of question types such as checkboxes, multiple choice, dropdowns, short answer and long text.</p>
<p>Forms can be shared via link to anyone, or even directly sent to specific users on the Nextcloud or to the whole Nextcloud instance. For improved security it is possible to set an expiration date. There is no limitation on the amount of forms or questions users can create nor the links they can share or people who can reply. Forms is responsive and will look good on any device or screen.</p>
<p>Responses are visualized either as summary statistics with charts which make it easy to analyze the results, or as the individual responses if users need to dig deeper or have a small number of respondents. The export format is compatible with the format used by Google Forms to work with any tool or workflow designed for integration with that.</p>
<p>The Nextcloud Forms app is available for easy one-click installation in the Nextcloud app store.</p>
