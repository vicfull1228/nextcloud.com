<h1>Nextcloud and Hancom partner up to provide users a secure, real-time co-authoring experience</h1>
<p><em>Collaborative editing solution for security-sensitive customers who want to maintain control over their own data</em></p>
<p><em>October 3, 2020, Berlin, Seoul</em> - Nextcloud GmbH, the company behind the most deployed on-premises content collaboration platform, and Hancom Group which provides modern office productivity solutions, announce a strategic partnership to enhance the Nextcloud platform by integrating Hancom Office Online, the leading web-based productivity suite that allows users to work together to create and edit MS Office documents effortlessly.</p>
<p>Hancom is well-known internationally for providing the default mobile office suite experience in Samsung Galaxy/Dex devices and as an ongoing technology partnership with AWS WorkDocs supplying Hancom Office Online, a cloud-based collaborative Office applications. The cloud-based web applications allow users to create, edit, save, and share documents, spreadsheet and presentation simultaneously in real time using modern web browsers. The editing capability extends fully to Microsoft Office files with market leading roundtrip compatibility.</p>
<blockquote>With this Nextcloud partnership, Hancom will bring Hancom Office Online, its flagship cloud-based office productivity application into the world’s most popular on-premise content collaboration platform. Together, Hancom and Nextcloud will be able to create the optimized user experience for viewing, editing, creating Office files. In addition, this cooperation can offer market-leading software solutions that leverage the power of the Internet and provide users with a consistent, unified and productive experience regardless of computing platform</blockquote>
<p>-- Dr. Won Sok Yun, President of Hancom Group</p>
<blockquote>With Hancom Office Online, a unique and powerful solution for viewing and editing office documents becomes available to Nextcloud users and customers. The special combination of a familiar user interface, excellent document interoperability and enterprise features is certain to speak to many of our users. We look forward to deeper integration with Hancom Office solutions in the future.</blockquote>
<p>- Frank Karlitschek, CEO and founder of Nextcloud GmbH</p>
<h2>About Hancom Group</h2>
<p>Founded in 1990, today Hancom Group is a leader in creating innovative ecosystems that will lead the world through the convergence of technology. With its reach of 14 affiliate companies covering Hardware, Software, and the Finance industry, the Group’s mission is to create a “Convenient World, Connected World, and Safe &amp; Secure World.” Hancom has been providing productivity applications for 30 years. Their major international partners have included Samsung, Google, Amazon Web Services, Polycom, Mail.ru (Russia), iFLYTEK (China) and Fibercorp (Argentina).</p>
<h2>About Nextcloud</h2>
<p>Nextcloud Hub is the industry-leading, fully open source, on-premises team productivity platform, combining the easy user interface of consumer-grade cloud solutions with the security and compliance measures enterprises need. Nextcloud Hub brings together universal access to data through mobile, desktop and web interfaces with next-generation, on-premise secure communication and collaboration features like real-time document editing, chat and video calls, putting them under direct control of IT and integrated with existing infrastructure.</p>
<p>Nextcloud’s easy and quick deployment, open, modular architecture and emphasis on security and advanced federation capabilities enable modern enterprises to leverage their existing file storage assets within and across the borders of their organization. For more information, visit <a href="https://nextcloud.com">nextcloud.com</a> or follow <a href="https://twitter.com/Nextclouders">https://twitter.com/Nextclouders</a> on Twitter.</p>
