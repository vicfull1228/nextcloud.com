<h1>EU funds creation of first major European Solid provider for Enterprises</h1>
<h3>Nextcloud now compatible with WWW-inventors’ privacy initiative</h3>
<p><em>Berlin, Germany, April 14, 2021</em> – At Solid World April, the results of a project funded by the European Commission through NLnet and Next Generation Internet were presented. The project developed Solid compatibility for Nextcloud allowing it to act as a Solid server. The integration work allows users of the popular open source on-premises enterprise content collaboration platform to choose a safe place for their private data rather than public cloud services.</p>
<blockquote>It could be that the way Solid takes off is that it starts off with all the Nextcloud users just becoming Solid users. That bootstrap could be a very important part of the way Solid takes off.</blockquote>
<p>-- Sir Tim Berners-Lee, Solid Director at <a href="https://vimeo.com/solidworld/review/531907097/eda903e37f#t=57m22s" class="hyperlink">Solid World April</a></p>
<p>With approximately 400.000 online servers, some servicing millions of users, Nextcloud is available for hosting in-house or at hundreds of different providers like Deutsche Telekom and IONOS in Europe and across the globe. Nextcloud GmbH has a business model that does not rely on data mining or advertising, free from surveillance capitalism. The company has seen very strong organic growth over the last years and services hundreds of governments, universities and major enterprises throughout the entire world. Nextcloud's 100% open source policy ensures transparency about data management and security, giving governments and enterprises the certainty they need for use of Nextcloud with sensitive data.</p>
<blockquote>Technology is what defines our modern economy and shapes our society. It is critical for Europe to regain sovereignty, and the combination of a strong open source offering like Nextcloud with the visionary approach of Solid now available to users marks an important step forward.</blockquote>
<p>-- Michiel Leenaars, Director of Strategy at NLnet</p>
<p>The integration project was carried out by PDS Interop with the support of a community of open source developers. The project was first presented at the international Solid World conference in April, which included a presentation by Yvo Brevoort about the development project. The open source work to ensure compatibility and development is ongoing.</p>
<blockquote>This can be a big win-win situation for both projects, enabling many other organizations to deliver practical, ethical solutions to citizens all over the globe and bringing decentralization back to the web.</blockquote>
<p>-- Frank Karlitschek, CEO Nextcloud GmbH</p>
<h2>About Nextcloud</h2>
<p>Nextcloud Hub is the industry-leading, fully open source, on-premises team productivity platform, combining the easy user interface of consumer-grade cloud solutions with the security and compliance measures enterprises need. Nextcloud Hub brings together universal access to data through mobile, desktop and web interfaces with next-generation, on-premise secure communication and collaboration features like real-time document editing, chat and video calls, putting them under direct control of IT and integrated with existing infrastructure.</p>
<p>Nextcloud’s easy and quick deployment, open, modular architecture and emphasis on security and advanced federation capabilities enable modern enterprises to leverage their existing file storage assets within and across the borders of their organization. For more information, visit <a href="https://nextcloud.com">nextcloud.com</a> or follow <a href="https://twitter.com/Nextclouders">https://twitter.com/Nextclouders</a> on Twitter.</p>

