<h1>Gaia-X proves independence from US big tech, picks EU based collaboration platform</h1>
<p><em>Gaia-X doubles down on the original mission of independence from foreign Hyperscalers and shows tangible results</em></p>
<p><em>Berlin, August 17 2021</em> - Nextcloud GmbH, the company behind the worlds’ most deployed on-premises content collaboration platform announces collaboration with the GAIA-X project, is now the official digital collaboration platform for GAIA-X.</p>
<blockquote>As a large and diversified European endeavor, Gaia-X was looking for a flexible, functionally complete, and European-based collaboration platform that integrates well with other tools. Nextcloud facilitates extensive cross-organisational collaboration for us in a secure and compliant way.</blockquote>
<p><em>– Francesco Bonfiglio, CEO of Gaia-X AISBL</em></p>
<p>GAIA-X was imagined as a federated data infrastructure ecosystem for Europe. More than 250 members already came on board and decided to join forces around the same common objectives and endeavors of GAIA-X. Europe’s GAIA-X challenges the Big Tech cloud dominance and gives European companies more control over their data. In other words, it is Europe’s plan to reclaim data sovereignty, the driving motivation behind the project.</p>
<p>The joining of US hyperscalers Amazon Web Services, Google, and Microsoft called into question whether EU’s Gaia-X cloud platform depends on the participation of these foreign giants and makes unclear how Europe can maintain control of its digital future if it’s reliant on foreign companies for technology.</p>
<p>After a long selection process, GAIA-X board bypassed the products from the hyperscalers and selected a European-developed and hosted collaboration platform. The GAIA-X members will collaborate on the next generation, independent and federated infrastructure for Europe using Nextcloud, the most popular on-premises content collaboration platform, hosted at Europe’s largest hosting provider, IONOS.</p>
<blockquote>The choice for Nextcloud by GAIA-X shows Europe already has a strong alternative to the products of foreign hyperscalers. As a platform already used by tens of millions of users in government, education, finance and health care, Nextcloud is a proven platform that offers strong, real-time collaboration in a secure, compliant and privacy-respecting manner.</blockquote>
<p>-- Frank Karlitschek, CEO and Founder of Nextcloud</p>
<p>The new digital collaboration platform Nextcloud is now up and running and the first members are onboarded. Every member already has read-only access to <a href="https://community.gaia-x.eu/" class="hyperlink">https://community.gaia-x.eu/</a>.</p>
<p>The first Extraordinary General Assembly of the Gaia-X AISBL  already took place in June 2021 to nominate the new Board of Directors by using Nextcloud Polls as a voting system to establish the first Board of Directors for the next two years.</p>
<h2>About Nextcloud</h2>
<p>Nextcloud Hub is the industry-leading, fully open source, on-premises team productivity platform, combining the easy user interface of consumer-grade cloud solutions with the security and compliance measures enterprises need. Nextcloud Hub brings together universal access to data through mobile, desktop and web interfaces with next-generation, on-premise secure communication and collaboration features like real-time document editing, chat and video calls, putting them under direct control of IT and integrated with existing infrastructure.</p>
<p>Nextcloud’s easy and quick deployment, open, modular architecture and emphasis on security and advanced federation capabilities enable modern enterprises to leverage their existing file storage assets within and across the borders of their organization. For more information, visit <a class="hyperlink" href="https://nextcloud.com">nextcloud.com</a> or follow <a class="hyperlink" href="https://twitter.com/Nextclouders">@Nextclouders</a> on Twitter.</p>

