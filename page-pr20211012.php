<h1>Nextcloud and TrueNAS deliver Productivity and Privacy</h1>
<p><em>Powerful content collaboration platform enriches open source enterprise storage platform</em></p>

<p><em>Stuttgart, October 12 2021</em> - Nextcloud GmbH, the company behind the worlds' most deployed on-premises content collaboration platform, and iXsystems inc., developers of the industry’s number one open storage platform, announce a partnership to bring the full suite of Nextcloud Hub features to TrueNAS. Tens of thousands of TrueNAS systems already run Nextcloud and availability of a supported, well integrated offering will give larger organizations more confidence to deploy.</p>

<blockquote>As self-funded companies that share a strong open source philosophy, Nextcloud and TrueNAS are natural partners. Our mutual customers will benefit from an open and flexible platform with strong enterprise support capable of delivering efficient collaboration at any scale.</blockquote>
<p>-- Frank Karlitschek, CEO and Founder, Nextcloud</p>

<p>iXsystems’ TrueNAS systems deliver flexible open storage, scaling from small home servers to 20PB+ storage platforms. TrueCommand offers simple management with docker and cloud deployment options. Nextcloud Hub is available as a supported app on the TrueNAS platform and can be easily deployed on new installations. TrueNAS systems will offer the full Nextcloud Hub experience with Files, Groupware, Talk, and Collabora Online based office document editing. TrueNAS users will gain immediate access to easy, efficient document storage, sharing and real-time collaboration and communication, expandable with >200 integrated Nextcloud apps. </p>

<a href="https://nextcloud.com/media/TrueNAS-banner.jpg"><img src="https://nextcloud.com/media/TrueNAS-banner.jpg" class="img-responsive" /></a>

<p>Unlike traditional cloud productivity platforms from Google and Microsoft, all data is maintained within the user’s private infrastructure and is not accessible to anyone else. In addition, both Nextcloud and TrueNAS provide an extensive layer of security and compliance capabilities.  Storage capacity can be scaled without any per-user restrictions or additional license fees.</p>

<blockquote>Enterprise storage needs continue to explode, only overtaken by the growth of compliance and privacy challenges. iXsystems and Nextcloud are uniquely capable of delivering a platform that tackles these two challenges at once.</blockquote>
<p>-- Brett Davis, EVP, iXsystems</p>
<p>Nextcloud Hub for TrueNAS is available immediately, based on the current Nextcloud 22. TrueNAS 12.0 users (CORE and Enterprise) can deploy the Nextcloud plugin. Nextcloud and iXsystems also offer enterprise support options for business users. Find more details in our blogs: <a class="hyperlink" href="https://www.ixsystems.com/blog/nextcloud-and-truenas-collaborate-to-help-you-build-your-own-private-cloud">TrueNAS announcement</a> – <a class="hyperlink" href="https://nextcloud.com/blog/nextcloud-now-officially-supported-on-truenas/">Nextcloud announcement.</a></p>
<p>A video with a walk-through of the installation process and a short look at Nextcloud is available on the <a class="hyperlink" href="https://youtu.be/TgSiYpcZZPY">TrueNAS YouTube channel.</a></p>

<h2>About iXsystems and TrueNAS</h2>
<p>Through decades of expertise in system design and development of Open Source software (FreeNAS, FreeBSD, OpenZFS, and TrueNAS), iXsystems has become an innovation leader in high availability storage and servers powered by Open Source solutions. With over one million deployments and backed by the legendary ZFS file system, TrueNAS offers the stability and reliability required for Backup, Multimedia, Cloud Hosting, Virtualization, Hyper-converged Infrastructure, and much more. </p>
<p>Since the founding of iXsystems in 2002, thousands of companies, universities, and government organizations have come to rely on the company’s enterprise servers, TrueNAS Open Storage, and consultative approach to building IT infrastructure and Private Clouds with Open Source economics. </p>

<h2>About Nextcloud</h2>
<p>Nextcloud Hub is the industry-leading, fully open source, on-premises team productivity platform, combining the easy user interface of consumer-grade cloud solutions with the security and compliance measures enterprises need. Nextcloud Hub brings together universal access to data through mobile, desktop and web interfaces with next-generation, on-premise secure communication and collaboration features like real-time document editing, chat and video calls, putting them under direct control of IT and integrated with existing infrastructure.</p>
<p>Nextcloud’s easy and quick deployment, open, modular architecture and emphasis on security and advanced federation capabilities enable modern enterprises to leverage their existing file storage assets within and across the borders of their organization. For more information, visit <a class="hyperlink" href="https://nextcloud.com">nextcloud.com</a> or follow <a class="hyperlink" href="https://twitter.com/Nextclouders">@Nextclouders</a> on Twitter.</p>

