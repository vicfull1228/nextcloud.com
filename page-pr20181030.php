<h1>Nextcloud founder wins innovation award, donates prize money to open source diversity fund</h1>
<h3>Frank Karlitschek won the Reinhard von Koenig award, receiving Eur 20.000 which he donates to the start of the Nextcloud Include open source diversity fund set up with KDE e.V.</h3>

<p><em>Stuttgart, 30 October 2018.</em> Nextcloud founder and entrepreneur Frank Karlitschek has won the Reinhard von Koenig award for progress and technology. He has decided to use the 20.000 euro prize to fund an open source diversity initiative named Nextcloud Include. The funds will be managed by the KDE e.V., an open source promoting non-profit organization Frank has been board member of in the past. Previous winners of the Reinhard von Koenig award include employees from Daimler AG and Atlatec GmbH for work on self-driving cars.</p>

<p><a href="https://margit-stumpp.de/" rel="noopener" target="_blank">Margit Stumpp,</a> Member of the German Parliament (Bundestag) noted:
<blockquote>Equal opportunities and diversity are very important issues for the future, especially in technical professions. I am pleased that Nextcloud Include is a new important initiative that is focusing on these issues.</blockquote></p>


<p>The biennial Reinhard von Koenig award is named after inventor Baron Reinhard von Koenig (1899-1992) and awarded at his former family castle Fachsenfeld in Aalen, located in East WÃ¼rttemberg, the region with the largest number of globally leading companies in Germany. In 1982, Baron von Keunig donated his entire estate and assets to the Fachsenfeld Foundation he founded, with the goal of promoting technological and cultural innovations. The criteria for the award requires that the prize is awarded to people who have developed new technological approaches to socially relevant challenges, shown technical courage and made a significant contribution to sustainable progress.</p>

<blockquote>I'm extremely honored to be awarded this prize, as a recognition of the incredible impact privacy issues have on our society and the importance of Nextcloud in providing a solution,</blockquote>
said Frank Karlitschek, entrepreneur, founder of Nextcloud and winner of the award.

<blockquote>by donating the prize money to a diversity goal, I hope it will help catalyze another transformation that society needs.</blockquote>

<p>The Nextcloud Include initiative aims to help underrepresented groups participate in the global Nextcloud community and foster an inclusive and diverse space where the community can continue to collaborate and develop world-class software. Mentoring, travel support and internships are provided as part of the program. The program is ran in collaboration with the KDE community under the umbrella of the KDE e.V. non-profit organization..</p>

<blockquote>We'd like to congratulate Frank on winning this prize and his decision to put the money to a great cause in open source,</blockquote>
said Lydia Pintscher, president of the board of KDE e.V.

<blockquote>For us, collaborating with Nextcloud in this way is a bit of a homecoming as it is one of the most successful projects to emerge from our community over the past decade.</blockquote>
<p>Nextcloud, Frank and KDE invite community members who would like to get involved in Nextcloud but face significant social hurdles to engage with our Include team. Information can be found on <a href="https://nextcloud.com/include">nextcloud.com/include</a></p>

