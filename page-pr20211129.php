<h1>EU coalition urges EU to push back against gate keeping by Microsoft, files official complaint</h1>
<p><em>November 29, Brussels</em> - A coalition of EU software and cloud businesses joined Nextcloud GmbH in respect of their formal complaint to the European Commission about Microsoft’s anti-competitive behavior in respect of its OneDrive (cloud) offering. In a repeat from earlier monopolistic actions, Microsoft is bundling its OneDrive, Teams and other services with Windows and aggressively pushing consumers to sign up and hand over their data to Microsoft. This limits consumer choice and creates a barrier for other companies offering competing services.</p>
<p>Over the last few years have grown their market share to 66% of the EU market, while local providers lost out from 26 to 16%. By heavily favoring their own products and services (so-called “self-preferencing”) or outright blocking other vendors they leverage their position as gate keepers to extend their reach in more and more neighbouring markets and push users deeper into their ecosystems. Local, more specialised vendors are unable to compete “on the merits” as the key to success is not a good product but the ability to distort competition and block market access.</p>
<blockquote>
<p>This is quite similar to what Microsoft did when it killed competition in the browser market, stopping nearly all browser innovation for over a decade. Copy an innovators’ product, bundle it with your own dominant product and kill their business, then stop innovating. This kind of behavior is bad for the consumer, for the market and, of course, for local businesses in the EU. Together with the other members of the coalition, we are asking the antitrust authorities in Europe to enforce a level playing field, giving customers a free choice and to give competition a fair chance.</p>
</blockquote>
<p>– Frank Karlitschek, CEO and founder of Nextcloud GmbH</p>
<p>The European Commission’s Directorate-General for Competition exists precisely for the purpose of preventing this kind of abusive behavior and keeping the market competitive and fair for all players. Nextcloud GmbH has filed an official complaint with the European Commission’s Directorate-General for Competition about the abusive practices of Microsoft related to OneDrive. Nextcloud GmbH has also filed a request with the German anti-trust authorities (the “Bundeskartellamt”) for an investigation against Microsoft and is discussing a complaint in France with its coalition members.</p>
<p>Dozens of European SMEs organisations support these efforts to push back against Big Tech and create a level playing field, supporting innovation and local (European) businesses. A full list of these companies as well as non-profits and industry consortia can be found on <a href="https://antitrust.nextcloud.com">https://antitrust.nextcloud.com</a></p>
