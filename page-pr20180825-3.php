<h1>Nextcloud lowers barrier to privacy-protecting personal cloud, introducing Simple Signup</h1>
<h3>Easy login flow makes signing up to a decentralized, federated file sync and share solution as simple as to established, centralized cloud services</h3>
<p><em>Berlin, August 25 2018.</em> Nextcloud GmbH, the globally leading supplier of self-hosted Content Collaboration Platforms, announces Simple Signup is now available on the website and mobile apps so users can sign up with just a few clicks to one of a select number of Nextcloud hosting providers.</p>

<p>With privacy violations and data leaks from centralized file handling solutions frequently in the news, there is a great need for better privacy protecting file store and share technologies. Nextcloud offers the most popular self-hosted alternative, with hundreds of thousands of servers forming a decentralized, federated network hosting data from an estimated over 25 million users.</p>

<p>Most home users lack the time and skills to run a private server, and several hundred providers offer users trusted hosted solutions. An account or complete private virtual server gives users choice but still requires them to choose, purchase and configure a private cloud.</p>

<p>The Nextcloud Simple Signup program eliminates this final barrier, merely requiring users to provide an email address and choose a password to be set up with a free account with at least 2 gigabyte of storage at a vetted Nextcloud hosting provider close to them.<p>

<blockquote>Making privacy as easy as the convenient but leaky alternatives has long been the goal of the Nextcloud contributors and we're very excited that, today, we succeeded. With this program we can truly change the dynamic on the internet, free the trillions of terrabytes of data stored at just one of a handful of large corporations and spread it out, as the internet was once designed to do.</blockquote>
said Frank Karlitschek, founder and CEO of Nextcloud GmbH.<br/>

<p>We go live with six providers.</p>

<blockquote>Simple Signup gives users the oportunity to quickly and easily get their real private cloud account without the need to run and maintain their own server while the data is completely under users control. Privacy and security are our top priorities when it comes to private data, that is why we keep our infrastructure in well proven, ISO27001 and GDPR compliant datacenters within the EU region. Your data will rest safe while remote daily backups will make sure nothing gets lost even in worst case scenario.</blockquote>
said Andrej Stojanoviƒç, CEO at <a href="https://webo.hosting/">Webo Hosting.</a><br/>

<blockquote>After running Nextcloud servers for bigger associations and companies already, we are excited to open up our portfolio to smaller teams and individuals as well. With the Simple Signup program, account setup and configuration works like a breeze and we look forward to providing safe, private cloud storage in Switzerland to our new audience.</blockquote>
said Andri Steiner, <a href="https://snowflakeops.ch">Snowflake Ops Ag</a><br/>

<blockquote>Thanks to Simple Signup, users can regain control over their data without
