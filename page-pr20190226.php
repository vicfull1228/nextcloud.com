<h1>Qloud MSP and Nextcloud announce partnership</h1>
<h3>Qloud MSP provides Cloud Data and Private Cloud Hosting Services.</h3>

<p><em>Stuttgart, Kuala Lumpur, 1st March 2019</em> - Nextcloud GmbH, the globally leading supplier of the most popular self-hosted content collaboration platform, announces a partnership with Qinetics Services Sdn Bhd d/b/a Qloud MSP to deliver secure, self-hosted cloud collaboration technology to customers in Malaysia and South East Asia. Together, Nextcloud and Qloud MSP enable customers to regain control over their data with a reliable, easy to use and well-integrated platform.</p>

<blockquote>The partnership with Qloud MSP gives our customers access to a secured data host, sync, share and collaboration environment in Malaysia,</blockquote> said Frank Karlitschek, CEO of Nextcloud GmbH. <blockquote>We look forward to successfully deliver projects together to empower customers and protect their data.</blockquote>

<blockquote>Nextcloud complements our portfolio to deliver private cloud solutions,</blockquote> says CW Wang, General Manager of Qloud MSP. <blockquote>We're proud to work together with the market leader in the self-hosted productivity market and deliver reliable, well integrated team productivity solutions to our customers.</blockquote>

<p>Modern organizations provide their employees with easy to use, mobile team productivity technology to enable on-the-go collaboration and sharing. As the leading self-hosted secure file exchange and collaboration solution, Nextcloud simplifies legal compliance and keeps data secure by providing system administrators full control over the location and access rights of stored data. Deep integration in infrastructure means quick deployment and little need for extensive data migration efforts. Qloud MSP offers its customers accelerated deployment, cloud or private hosting and 24/7 Support.</p>


<p><strong>About Nextcloud</strong><br>
Nextcloud offers the industry-leading, fully open source, self-hosted Content Collaboration Platform, combining the easy user interface of consumer-grade cloud solutions with the security and compliance measures enterprises need. Nextcloud brings together universal access to data through mobile, desktop and web interfaces with next-generation, on-premise secure communication and collaboration features like real-time document editing, chat and video calls, putting them under direct control of IT and integrated with existing infrastructure.<br/>
Nextcloud's easy and quick deployment, open, modular architecture and emphasis on security and advanced federation capabilities enable modern enterprises to leverage their existing file storage assets within and across the borders of their organization. For more information, visit <a href="https://nextcloud.com">nextcloud.com</a> or follow <a href="https://twitter.com/Nextclouders">@Nextclouders</a> on Twitter.</p>

<p><strong>About QLoud MSP</strong><br>
Qloud MSP (Qinetics Services Sdn. Bhd) is a Malaysian based technology company specialized in Managed Services, IT outsourcing and Cloud Computing. Our solution is highly focus in driven customers business productivity while prioritizing data protection and control.</p>

