<h1>Nextcloud launches Customer Advisory Board</h1>
<p><em>Stuttgart, January 28 2018</em> - Nextcloud GmbH, supplier of the most popular self-hosted content collaboration platform, introduces a Customer Advisory Board to help guide its development and decision making. The Advisory Board provides a platform for Nextcloud's Premium customers to have regular, direct communication with the Nextcloud management team, providing an opportunity to discuss their strategic needs and goals.</p>

<p>The Advisory Board will meet twice a year, on the evening before the bi-annual Nextcloud Enterprise day, for a dinner, presentation and discussion about strategic challenges. Quarterly conference calls and other communication channels will provide regular opportunities to discuss the roadmap and market developments.</p>

<p>Each Premium Subscription customer gets a seat on the advisory board. A selection of Nextcloud's largest customers will complete this board. The Advisory Board offers our customers a unique insight in upcoming technologies and a chance to share needs and concerns with Nextcloud management. We have begun reaching out to our customers to join the board and welcome all existing and new Premium subscribers to participate.</p>

<p>The Advisory board will officially be launched at the upcoming Nextcloud Enterprise Day in Stuttgart, March 12 2019.</p>

<p>Through our Advisory Board, our customers are part of building a vision for secure file exchange and teamwork in organizations of the future!</p>

<p>Two more announcements today cover the upcoming Virtual Drive technology in the Nextcloud Desktop Client and the growing momentum of Nextcloud GmbH, which more than doubled its customer base in 2018 amid growing interest in compliance solutions to secure and control data.</p>

<p><strong>About Nextcloud</strong><br>
Nextcloud offers the industry-leading, fully open source, self-hosted Content Collaboration Platform, combining the easy user interface of consumer-grade cloud solutions with the security and compliance measures enterprises need. Nextcloud brings together universal access to data through mobile, desktop and web interfaces with next-generation, on-premise secure communication and collaboration features like real-time document editing, chat and video calls, putting them under direct control of IT and integrated with existing infrastructure.<br/>
Nextcloud's easy and quick deployment, open, modular architecture and emphasis on security and advanced federation capabilities enable modern enterprises to leverage their existing file storage assets within and across the borders of their organization. For more information, visit <a href="https://nextcloud.com">nextcloud.com</a> or follow <a href="https://twitter.com/Nextclouders">@Nextclouders</a> on Twitter.</p>


