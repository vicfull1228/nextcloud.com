        <h2>Event details leaked when sharing a non-public calendar event (NC-SA-2020-013)</h2>
        <p>15th November 2018</p>
        <p>Risk level: <strong>Low</strong></p>
        <p>CVSS v3 Base Score: 4.8 (<a href="https://www.first.org/cvss/calculator/3.0#CVSS:3.0/AV:N/AC:H/PR:L/UI:R/S:U/C:H/I:N/A:N">AV:N/AC:H/PR:L/UI:R/S:U/C:H/I:N/A:N</a>)</p>
        <p>CWE: <a href="https://cwe.mitre.org/data/definitions/281.html">Improper Preservation of Permissions (CWE-281)</a></p>
        <p>HackerOne report: <a href="https://hackerone.com/reports/439828">439828</a></p>
        <h3>Description</h3>
        <p>Improper preservation of permissions in Nextcloud Server 14.0.3 causes the event details to be leaked when sharing a non-public event.</p>
        <h3>Affected Software</h3>
        <ul>
            <li>Nextcloud Server &lt; <strong>14.0.4</strong> (CVE assignment pending)</li>
<li>Nextcloud Server &lt; <strong>13.0.8</strong> (CVE assignment pending)</li>
<li>Nextcloud Server &lt; <strong>12.0.13</strong> (CVE assignment pending)</li>

        </ul>
        <h3>Action Taken</h3>
        <p>The error has been fixed.</p>
        <h3>Resolution</h3>
        <p>It is recommended that the Nextcloud Server is upgraded to 14.0.4.</p>
