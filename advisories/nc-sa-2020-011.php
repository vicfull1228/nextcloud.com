        <h2>Name of private conversations leaked when linked via projects to a shared item (NC-SA-2020-011)</h2>
        <p>29th July 2019</p>
        <p>Risk level: <strong>Low</strong></p>
        <p>CVSS v3 Base Score: 2 (<a href="https://www.first.org/cvss/calculator/3.0#CVSS:3.0/AV:N/AC:H/PR:H/UI:R/S:U/C:L/I:N/A:N">AV:N/AC:H/PR:H/UI:R/S:U/C:L/I:N/A:N</a>)</p>
        <p>CWE: <a href="https://cwe.mitre.org/data/definitions/284.html">Improper Access Control (CWE-284)</a></p>
        <p>HackerOne report: <a href="https://hackerone.com/reports/662218">662218</a></p>
        <h3>Description</h3>
        <p>Improper access control in Nextcloud Talk 6.0.3 leaks the existance and the name of private conversations when linked them to another shared item via the projects feature.</p>
        <h3>Affected Software</h3>
        <ul>
            <li>Nextcloud Talk &lt; <strong>6.0.4</strong> (CVE-2019-15620)</li>

        </ul>
        <h3>Action Taken</h3>
        <p>The error has been fixed.</p>
        <h3>Resolution</h3>
        <p>It is recommended that the Nextcloud Talk 6.0.3 is upgraded to 6.0.4.</p>
