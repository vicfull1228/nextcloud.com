        <h2>Improper access control checks for single share previews (NC-SA-2018-014)</h2>
        <p>25th October 2018</p>
        <p>Risk level: <strong>Low</strong></p>
        <p>CVSS v3 Base Score: 4.8 (<a href="https://www.first.org/cvss/calculator/3.0#CVSS:3.0/AV:N/AC:H/PR:N/UI:N/S:U/C:L/I:L/A:N">AV:N/AC:H/PR:N/UI:N/S:U/C:L/I:L/A:N</a>)</p>
        <p>CWE: <a href="https://cwe.mitre.org/data/definitions/287.html">Improper Authentication (CWE-287)</a></p>
        <p>HackerOne report: <a href="https://hackerone.com/reports/231917">231917</a></p>
        <h3>Description</h3>
        <p>A missing check could give unauthorized access to the previews of single file password protected shares.</p>
        <h3>Affected Software</h3>
        <ul>
            <li>Nextcloud Server &lt; <strong>14.0.0</strong> (CVE assignment pending)</li>

        </ul>
        <h3>Action Taken</h3>
        <p>The error has been fixed.</p>
        <h3>Resolution</h3>
        <p>It is recommended that all instances are upgraded to Nextcloud 14.0.0.</p>
