        <h2>Improper access control allows injecting tasks into other users decks (NC-SA-2020-022)</h2>
        <p>15th May 2020</p>
        <p>Risk level: <strong>Low</strong></p>
        <p>CVSS v3 Base Score: 4.1 (<a href="https://www.first.org/cvss/calculator/3.0#CVSS:3.0/AV:N/AC:L/PR:L/UI:R/S:C/C:N/I:L/A:N">AV:N/AC:L/PR:L/UI:R/S:C/C:N/I:L/A:N</a>)</p>
        <p>CWE: <a href="https://cwe.mitre.org/data/definitions/284.html">Improper Access Control - Generic (CWE-284)</a></p>
        <p>HackerOne report: <a href="https://hackerone.com/reports/867052">867052</a></p>
        <h3>Description</h3>
        <p>Improper access control in Nextcloud Deck 1.0.0 allowed an attacker to inject tasks into other users decks.</p>
        <h3>Affected Software</h3>
        <ul>
            <li>Nextcloud Deck &lt; <strong>1.0.1</strong> (CVE-2020-8179)</li>

        </ul>
        <h3>Action Taken</h3>
        <p>The error has been fixed.</p>
        <h3>Resolution</h3>
        <p>It is recommended that the Nextcloud Deck is upgraded to 1.0.1.</p>
