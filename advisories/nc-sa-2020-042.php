        <h2>Improper access control to messages of Social app (NC-SA-2020-042)</h2>
        <p>15th October 2020</p>
        <p>Risk level: <strong>Low</strong></p>
        <p>CVSS v3 Base Score: 5 (<a href="https://www.first.org/cvss/calculator/3.0#CVSS:3.0/AV:N/AC:L/PR:L/UI:N/S:C/C:L/I:N/A:N">AV:N/AC:L/PR:L/UI:N/S:C/C:L/I:N/A:N</a>)</p>
        <p>CWE: <a href="https://cwe.mitre.org/data/definitions/284.html">Improper Access Control - Generic (CWE-284)</a></p>
        <p>HackerOne report: <a href="https://hackerone.com/reports/921717">921717</a></p>
        <h3>Description</h3>
        <p>Improper access control in Social app 0.3.1 allowed to read posts of any user.</p>
        <h3>Affected Software</h3>
        <ul>
            <li>Nextcloud Social &lt; <strong>0.4.0</strong> (CVE-2020-8278)</li>

        </ul>
        <h3>Action Taken</h3>
        <p>The error has been fixed.</p>
        <h3>Resolution</h3>
        <p>It is recommended that the Social app is upgraded to 0.4.0.</p>
