<h1>ONLYOFFICE and Nextcloud partner to deliver seamless collaboration for modern organizations</h1>
<h3>Modern online office suite perfect fit with most popular self-hosted content collaboration platform</h3>

<p><em>November 20, 2018 - Latvia & Stuttgart.</em> We are excited to share news of a partnership between Ascensio System SIA, the Latvian home of the ONLYOFFICE real-time online document editor, and Nextcloud GmbH, the German company developing the market-leading self-hosted Content Collaboration Platform for secure file exchange, collaboration and communication.</p>

<p>Through this partnership, enterprises gain access to a lightweight, modern browser-based document editor fully compatible with Microsoft Office formats, seamlessly integrated in a powerful file exchange and communication platform. The combined solution allows organizations to empower their employees to efficiently collaborate across organizational boundaries and on the go while maintaining strict control over data access to ensure compliance and security needs are met.</p>

<p>With over 5 million users, ONLYOFFICE is a proven innovator in the online office business, first to market with a full HTML5 based online document editor. Built on a cutting-edge technology, it offers advanced features including change tracking, version history and rights management. With a self-hosted solution, enterprises can be confident data never leaves the control sphere of their IT department.</p>

<p>The real-time document editing capabilities of ONLYOFFICE integrated into Nextcloud enable users to exchange and collaborate on text documents, spreadsheets and presentations, editing them simultaneously with others from within Nextcloud.</p>

Galina Godukhina, Commercial Director at ONLYOFFICE:
<blockquote>Our partnership with Nextcloud delivers customers integrated, real-time document collaboration to improve the productivity of modern, distributed teams who need to work together across organizational boundaries and on the go.</blockquote>

<p>ONLYOFFICE fits in a wide range of productivity and collaboration capabilities in Nextcloud, empowering enterprises to regain control over their document exchange and communication with self-hosted integrated audio/video calls and chat, mail and calendaring with an easy web interface and mobile applications.</p>

<p>As a part of a healthy ecosystem of over 130 third-party apps, ONLYOFFICE is now available for Nextcloud customers through an optional add-on package providing the services and support enterprises can rely on.</p>

Jos Poortvliet, Head of Marketing at Nextcloud:
<blockquote>Hosting Nextcloud and ONLYOFFICE on premises or at a qualified, trusted, local provider enables enterprises to regain control over their data and communication while retaining the productivity benefits of the cloud.</blockquote>

<p><strong>About ONLYOFFICE</strong><br>
ONLYOFFICE by Ascensio System SIA provides users with a powerful office suite that comprises online editors for text documents, spreadsheets and presentations highly compatible with Microsoft Office, OpenDocument and all other popular file formats.
With ONLYOFFICE users get a vast range of collaborative features to edit docs with team members in real time: two co-editing modes, commenting, built-in chat, the Track Changes mode including Preview in Review feature to know what a doc would look like with all changes accepted or rejected, version history and more. <br/>
The ONLYOFFICE suite allows integrating the editors with your favorite cloud storage services, as well as extending functionality of the web solution you are building yourself. Learn more at <a href="https://www.onlyoffice.com/">www.onlyoffice.com</a>.<br/>

<p><strong>About Nextcloud</strong><br>
Nextcloud offers the industry-leading, fully open source, self-hosted Content Collaboration Platform, combining the easy user interface of consumer-grade cloud solutions with the security and compliance measures enterprises need. Nextcloud brings together universal access to data through mobile, desktop and web interfaces with next-generation, on-premise secure communication and collaboration features like real-time document editing, chat and video calls, putting them under direct control of IT and integrated with existing infrastructure.<br/>
Nextcloud's easy and quick deployment, open, modular architecture and emphasis on security and advanced federation capabilities enable modern enterprises to leverage their existing file storage assets within and across the borders of their organization. For more information, visit <a href="https://nextcloud.com">https://nextcloud.com</a> or follow <a href="https://twitter.com/Nextclouders">@Nextclouders</a> on Twitter.</p>


