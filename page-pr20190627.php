<h1>Nextcloud introduces collaborative rich text editor</h1>

<h3>Upcoming version of leading self-hosted content collaboration platform delivers distraction-free collaborative writing out of the box</h3>

<p><em>Stuttgart, April 25 2018</em> - Nextcloud GmbH, supplier of the globally most deployed self-hosted content collaboration platform, announce Nextcloud Text, an easy to use, collaborative rich-text editor using Markdown under the hood. The first release is available immediately for Nextcloud 16 users, and it will be our standard text editor in Nextcloud 17.</p>
<p>In a world where distractions are everywhere, clean, focused text editors are becoming more popular. If you need to structure your thoughts, brainstorm an idea or take notes, the million-and-one features of a word processor just hold you back.</p>
<p>Nextcloud Text lets the content take center stage. That is not to say Nextcloud Text is not a capable editor. Besides the basic markup like Bold, italics &amp; headers, you can insert images and we plan to support tables, mentioning other users and more. It is a fully collaborative editor, letting any number of people work on a document in real time.</p>
<p>Text uses the sidebar integration announced as part of Nextcloud 15, giving it access to the same sidebar available when editing documents in Collabora Online introduced December 2018. Users can  easily access sharing, comments and older versions. They can also have a call or chat in the sidebar with the recipient(s) of their shared documents.</p>
<p>Nextcloud Text uses Markdown, a minimalist writing system, designed to let you write your thoughts down and structure them without all the overhead that comes with styling. It uses plain text with some obvious, modifiers to make lists, emphasize things and show some structure. Nextcloud Text is based on ProseMirror and tiptap and we thank their developers for their amazing work.</p>

<p>Today, version 1.0 of Nextcloud Text is available in our app store and users can download it for Nextcloud 16. Over the next weeks will continue to improve the app, both in terms of features, design and of course stability. When Nextcloud 17 is released, the plan is that Nextcloud Text will replace the current text editor, bringing collaborative rich-text editing to Nextcloud out-of-the-box.</p>

