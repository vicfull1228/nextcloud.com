<h1>Nextcloud and VIBE Cybersecurity Join Forces to Launch Crypto as a Service</h1>
<h3>Leading open-source provider to secure its entire application stack with modern cryptosystem</h3>
<p><em>January 18, 2021</em> - Nextcloud and VIBE Cybersecurity International today announced a partnership in which the companies will offer VIBE Cybersecurity’s patented cryptosystem in Nextcloud’s industry- leading, on-premises, content-collaboration platform. Offered as Nextcloud HUB, the platform delivers all the essential tools required to operate a business today, including Nextcloud Files (Enterprise File Sync and Share), Nextcloud Talk (Calls, Chats and Web Meetings), and Nextcloud Groupware (Calendar, Contacts and mail).</p>

Nextcloud CEO, Frank Karlitschek, stated:
<blockquote>We are delighted to enter into this partnership with VIBE Cybersecurity to offer our customers a highly advanced encryption offering, rendering data security truly ironclad end-to-end by seamlessly securing all data in transit, and allowing for extension to secure the client server end in our NC20 release. On the VIBE roadmap is also securing Nextcloud Talk and Groupware for our next major release, allowing the Nextcloud platform to raise the bar when it comes to providing authenticated, end-to-end encryption.</blockquote>

<p>VIBE Cybersecurity’s cryptosystem, patented as Verifiable Identity-Based Encryption (VIBE), substantially improves upon the IBE standard, rendering this market-proven protocol ideal for today’s digital world. VIBE is certificateless, and delivers the added value of authenticating at the application layer. Packaged as Crypto-as-a-Service, it is very easy to deploy and use, does not require sophisticated technical or security skills for system administrators, and also provides immense logistical value as a vehicle to deliver flexible, seamless key deployment and key management infrastructures. </p>

VIBE Cybersecurity’s CEO, Bill Montgomery, commented,
<blockquote>We are honoured to join the Nextcloud partner community, and look forward to helping Nextcloud become a world leader in delivering ironclad end-to-end encryption, and authentication. Nextcloud customers who adopt this technology can rest easy, knowing their data and their businesses are fully protected.</blockquote>

Nextcloud CEO, Frank Karlitschek, stated:
<blockquote>Companies that protect their data with VIBE inside Nextcloud will have the added comfort of knowing that they are GDPR-compliant, with full audit trails available to them to validate the security and privacy measures they have taken to protect their customers’ personal data.</blockquote>

<p>The Nextcloud VIBE offer will begin with Windows, followed by the other major OS, with a rich set of additional capabilities, features and client support scheduled for introduction throughout 2021.</p>

<h2>About Nextcloud</h2>
<p>Nextcloud Hub is the industry-leading, fully open source, on-premises team productivity platform, combining the easy user interface of consumer-grade cloud solutions with the security and compliance measures enterprises need. Nextcloud Hub brings together universal access to data through mobile, desktop and web interfaces with next-generation, on-premise secure communication and collaboration features like real-time document editing, chat and video calls, putting them under direct control of IT and integrated with existing infrastructure.</p>
<p>Nextcloud’s easy and quick deployment, open, modular architecture and emphasis on security and advanced federation capabilities enable modern enterprises to leverage their existing file storage assets within and across the borders of their organization. For more information, visit <a href="https://nextcloud.com">nextcloud.com</a> or follow <a href="https://twitter.com/Nextclouders">https://twitter.com/Nextclouders</a> on Twitter.</p>
<h2>About VIBE Cybersecurity</h2>

<p>VIBE Cybersecurity International’s core offering, Verifiable Identity-Based Encryption (VIBE), is a modern certificate-less, authenticating, cryptosystem that completely disrupts today’s security landscape, and affords organizations which embrace it the opportunity to eliminate their reliance on vulnerable, costly, complex certificate-based technologies. VIBE is patented in the US, UK, EU and China, and has partnership agreements in place with leading companies which are poised to bring its modern, break-through, Crypto-as-a-Service offering to market worldwide.  VIBE Cybersecurity’s mission is to prevent the cyberattacks that are crippling governments, businesses and the people they serve and protect, thereby rendering our world a safer place. For more information visit https://vibecyber.com or send an email message to info@vibecyber.com. </p>

