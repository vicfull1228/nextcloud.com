<h1>Nextcloud and Kaspersky partner up to protect users from malicious files</h1>
<p><em>Integration will offer automated scanning of files to ensure file sharing does not become a vector for infection to spread</em></p>
<p>August 31, 2020 – Nextcloud GmbH, the company behind the worlds’ most deployed on-premises content collaboration platform, and Kaspersky, the world’s largest privately-held vendor of endpoint protection and cybersecurity solutions, announce a partnership to help secure customer data. With online collaboration tools becoming central to remote and distributed work in modern organizations, risks of malicious files spreading over the internal network have to be mitigated. The secure, on-premises content collaboration technology from Nextcloud integrates Kaspersky Scan Engine, the company’s integrated security product to automatically scan files when they are uploaded and ensure no dangerous files can spread.</p>
<blockquote>
<p>Nextcloud strives to provide the most secure online collaboration platform on the market. By integrating Kaspersky’s powerful protection capabilities, enterprises can rest assured malicious content can not easily spread through their document exchange technology.</p>
</blockquote>
<p>– Frank Karlitschek, CEO of Nextcloud GmbH</p>
<p>The cross-platform nature of Nextcloud, with clients on mobile and desktop operating systems, means users can unknowingly upload and share infected files that might not affect their device, or even intentionally try to spread them through a public Nextcloud instance. The integrated anti-virus scan will intercept and block the upload on the server side, making sure the file is not distributed to other users.</p>
<blockquote>
<p>“The integration of Kaspersky anti-malware technology into Nextcloud’s product provides its customers with the certainty that files accessed through or downloaded from their content collaboration platform will be secure. It is extremely difficult for businesses to fight millions of web threats on their own – that’s why technology partners are needed. Together we can make our online world safer,”</p>
</blockquote>
<p>– Alexander Karpitsky, Head of Kaspersky Technology Alliances, Kaspersky.</p>
<p>Enterprise customers can install the Nextcloud Anti-virus application from the app store and contact Nextcloud GmbH for support and the required Kaspersky Scan Engine component</p>
<h2>About Kaspersky</h2>
<p>Kaspersky is a global cybersecurity company founded in 1997. Kaspersky’s deep threat intelligence and security expertise is constantly transforming into innovative security solutions and services to protect businesses, critical infrastructure, governments and consumers around the globe. The company’s comprehensive security portfolio includes leading endpoint protection and a number of specialized security solutions and services to fight sophisticated and evolving digital threats. Over 400 million users are protected by Kaspersky technologies and we help 250,000 corporate clients protect what matters most to them. Learn more at &lt;www.kaspersky.com&gt;</p>
<h2>About Nextcloud</h2>
<p>Nextcloud develops a fully open source solution for on-premise data handling and communication with an uncompromising focus on security and privacy and unprecedented scalability. Nextcloud brings together universal access to data with next-generation secure communication and collaboration capabilities under direct control of IT and integrated with existing compliant infrastructure. Nextcloud’s open, modular architecture, emphasis on security and advanced federation capabilities enable modern enterprises to leverage their existing assets within and across the borders of their organization. For more information, visit <a href="https://nextcloud.com">https://nextcloud.com</a> or follow <span class="citation" data-cites="Nextclouders">@Nextclouders</span> on Twitter.</p>
